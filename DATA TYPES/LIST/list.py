"""
LIST

---> Sequence data type
---> Used to store multiple values in a single variable
---> Lists created using square brackets
---> A list is a collection of things,enclosed in [] and seperated by commas
---> List items are ordered(order will not change)
---> Changeable(we can change,add,remove items in the list after creation)
---> allows duplicates(items with same value)
---> List items are indexed(indexing: process of accessing a specific element in a sequence using its position or index value)
---> index range from 0 to -1
    --> first item => [0]
    --> second item => [1]
---> Syntax
    list_var=[list_value1,list_value2,list_value3,list_value4,list_value5]
      
                 0             1           2           3           4         (indexing)


                 -5            -4          -3          -2          -1        (negative indexing)



"""

